# Different forms of defining strings 

# 1 -:
name1 = "Jhon"

# 2 -:
name2 = 'Jhon'

# 3-:
name3 = "Jhon's"

# 4 -:
name4 = "Jhon\nMy name"
print("4 :\n",name4)

# 5 -:
name5 = """
            Jhon
            My name
        """
print("5 :\n",name5)    

# 6 -:
name6 = r'Jhon\nMy name'            # Here "r" is for raw string and in raw string escaping is off
print("6 : \n",name6)